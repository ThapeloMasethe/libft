#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	unsigned char *s;

	s = (unsigned char *)str;
	while (n > 0)
	{
		if (*s != (unsigned char)c)
			s++;
		else
			return (s);
		n--;
	}
	return (0);
}
